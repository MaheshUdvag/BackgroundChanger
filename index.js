function cricket() {
    // this function is used to show the cricket background and hide the movie 
    // background
    document.getElementById("cricket").style.display = 'block';
    document.getElementById("movie").style.display = 'none';
}

function movie() {
    // this function is used to show the movie background and hide the cricket 
    // background
    document.getElementById("movie").style.display = 'block';
    document.getElementById("cricket").style.display = 'none';
}

